# George Boole - True and False -> 1 and 0
print(1 == 1) # return True
print(1 == 2) # returns False
print("python" == "python") # return True
print("Python" == "python") # return false
print(3 <= 4) # returns true

# logical Operator
# OR, AND, and NOT
# OR -> Only one expression must evaluate to True 
# AND -> both expressions must be True
# NOT -> Negates an expression
# AND operator
print((1 == 1) and (2 == 2)) # returns True
# OR operator
print((1 == 1) or (2 == 3)) # return True
# NOT operator
print(not(1 == 1)) # return False
print(not(1 == 2)) # returns True