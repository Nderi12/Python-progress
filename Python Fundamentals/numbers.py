# the abs() method
print(abs(5)) # return the value of the number we provide and 0
print(abs(100))

# comparing two numbers in python using max() and min()
x = 1
y = 2

largest = max(x, y)
smallest = min(x, y)
print(f"The largest number between {x} and {y} is {largest}")
print(f"The smallest number between {x} and {y} is {smallest}")

# raising to a power -> pow(x, y) where x is the base, and y is the exponent or power
a = 3 # base
b = 2 # exponent/power
power = pow(a, b)
print(f"The square of {a} raised to {b} is {power}")
